const gradeMap = {
  'c1': '1',
  'c2': '2',
  'c3': '3'
};

const unitMap = {
  'u1': '1', 'u2': '2', 'u3': '3', 'u4': '4',
  'u5': '5', 'u6': '6', 'u7': '7', 'u8': '8'
};

function processFile(filePath) {
  let content = fs.readFileSync(filePath, 'utf8');

  // 收集当前文件需要处理的lessonId，按文件中出现的顺序排列
  const fileGrade = filePath.includes('grade1') ? 'c1' :
                    filePath.includes('grade2') ? 'c2' : 'c3';

  const lessonIds = Object.keys(q7Data).filter(id => id.substring(0, 2) === fileGrade);

  // 按文件中出现的顺序排序（从后往前处理，避免位置偏移）
  const positions = [];
  for (const lessonId of lessonIds) {
    const q6Id = lessonId + 'q6';
    const q7Id = lessonId + 'q7';

    // 检查q7是否已存在
    if (content.includes(`id: '${q7Id}'`)) {
      console.log(`  跳过 ${q7Id}，已存在`);
      continue;
    }

    const q6Index = content.indexOf(`id: '${q6Id}'`);
    if (q6Index === -1) {
      console.log(`  警告：找不到 ${q6Id}`);
      continue;
    }

    // 查找practiceQuestions的结束模式
    // 有两种模式：
    // 1. "}\n        ]" (l1, l4, l5等课时)
    // 2. "}]" (l2, l3等课时，在同一行)
    let closeIndex = -1;
    let closePattern = '';

    // 先尝试模式1
    const pattern1 = '}\n        ]';
    const idx1 = content.indexOf(pattern1, q6Index);
    // 再尝试模式2
    const pattern2 = '}]';
    const idx2 = content.indexOf(pattern2, q6Index + 10); // 跳过q6 id本身附近的}]

    // 选择最近的匹配
    if (idx1 !== -1 && (idx2 === -1 || idx1 < idx2)) {
      closeIndex = idx1;
      closePattern = pattern1;
    } else if (idx2 !== -1) {
      closeIndex = idx2;
      closePattern = pattern2;
    }

    if (closeIndex === -1) {
      console.log(`  警告：找不到 ${q6Id} 后的practiceQuestions结束位置`);
      continue;
    }

    positions.push({ lessonId, closeIndex });
  }

  // 从后往前处理，避免位置偏移
  positions.sort((a, b) => b.closeIndex - a.closeIndex);

  for (const { lessonId, closeIndex } of positions) {
    const q7Id = lessonId + 'q7';
    const q7Content = q7Data[lessonId];
    const [question, answer, hintName, explanation] = q7Content;
    const gradeNum = gradeMap[lessonId.substring(1, 2)];
    const unitNum = unitMap[lessonId.substring(2, 4)];

    // 检查实际模式
    let oldText = '';
    let q7Block = '';

    if (content.substring(closeIndex, closeIndex + '}\n        ]'.length) === '}\n        ]') {
      // 模式1：多行格式
      oldText = '}\n        ]';
      q7Block = `},
          {
            id: '${q7Id}',
            type: 'fill',
            question: '${question}',
            answer: '${answer}',
            hint: '${hintName}（语文${gradeNum}年级-第${unitNum}单元）',
            explanation: '${explanation}'
          }
        ]`;
    } else if (content.substring(closeIndex, closeIndex + '}]'.length) === '}]') {
      // 模式2：单行格式
      oldText = '}]';
      q7Block = `},
          {
            id: '${q7Id}',
            type: 'fill',
            question: '${question}',
            answer: '${answer}',
            hint: '${hintName}（语文${gradeNum}年级-第${unitNum}单元）',
            explanation: '${explanation}'
          }
        ]`;
    } else {
      console.log(`  警告：${lessonId}q6 后的文本不匹配任何预期模式`);
      continue;
    }

    content = content.substring(0, closeIndex) + q7Block + content.substring(closeIndex + oldText.length);
    console.log(`  添加 ${q7Id}`);
  }

  fs.writeFileSync(filePath, content, 'utf8');
  console.log(`文件已更新: ${filePath}`);
}

