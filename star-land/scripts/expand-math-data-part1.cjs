// 将题目对象转为TS代码字符串
function qsToStr(questions) {
  return questions.map(q => {
    let s = `          {\n            id: '${q.id}',\n            type: '${q.type}',\n`;
    s += `            question: \`${q.question}\`,\n`;
    if (q.options) s += `            options: ${JSON.stringify(q.options)},\n`;
    s += `            answer: \`${q.answer}\`,\n`;
    s += `            hint: \`${q.hint}\`,\n`;
    s += `            explanation: \`${q.explanation.replace(/`/g, "'")}\`\n          }`;
    return s;
  }).join(',\n');
}

