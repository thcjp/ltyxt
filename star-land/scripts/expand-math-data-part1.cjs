// ============ 工具函数 ============
function q(id, type, question, options, answer, hint, explanation) {
  const obj = { id, type, question, answer, hint, explanation };
  if (options) obj.options = options;
  return obj;
}
function bq(id, t, qu, o, a, h, e) { return q(id,t,qu,o,a,h,e); } // 基础
function aq(id, t, qu, o, a, h, e) { return q(id,t,qu,o,a,h,e); } // 提高
function cq(id, t, qu, o, a, h, e) { return q(id,t,qu,o,a,h,e); } // 挑战
function rq(id, t, qu, o, a, h, e) { return q(id,t,qu,o,a,h,e); } // 回溯
