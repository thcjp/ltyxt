// 插入题目到指定lesson的practiceQuestions末尾
function addQ(lessonId, questions) {
  const marker = `id: '${lessonId}'`;
  const pos = c.indexOf(marker);
  if (pos === -1) { console.log(`SKIP ${lessonId}`); return; }
  // 找到该lesson的practiceQuestions数组
  const pqPos = c.indexOf('practiceQuestions: [', pos);
  if (pqPos === -1) { console.log(`SKIP PQ ${lessonId}`); return; }
  // 找到practiceQuestions数组结束的位置 - 找到匹配的 ]
  let depth = 0, i = pqPos + 'practiceQuestions: ['.length - 1;
  depth = 1;
  i++;
  while (i < c.length && depth > 0) {
    if (c[i] === '[') depth++;
    if (c[i] === ']') depth--;
    i++;
  }
  const insertPos = i - 1; // 在 ] 之前插入
  const qCode = questions.map(q => {
    let s = `,\n          {\n            id: '${q.id}',\n            type: '${q.type}',\n`;
    s += `            question: '${q.q}',\n`;
    if (q.opts) s += `            options: [${q.opts.map(o => `'${o}'`).join(', ')}],\n`;
    s += `            answer: '${q.a}',\n            hint: '${q.h}',\n            explanation: '${q.e}'\n          }`;
    return s;
  }).join('');
  c = c.substring(0, insertPos) + qCode + c.substring(insertPos);
  console.log(`OK ${lessonId} +${questions.length}Q`);
}

// 插入新课时到指定unit的lessons数组末尾
function addLesson(unitId, lessonCode) {
  const marker = `id: '${unitId}'`;
  const pos = c.indexOf(marker);
  if (pos === -1) { console.log(`SKIP UNIT ${unitId}`); return; }
  // 找到该unit的lessons数组
  const lPos = c.indexOf('lessons: [', pos);
  if (lPos === -1) { console.log(`SKIP LESSONS ${unitId}`); return; }
  // 找到lessons数组结束位置
  let depth = 0, i = lPos + 'lessons: ['.length - 1;
  depth = 1;
  i++;
  while (i < c.length && depth > 0) {
    if (c[i] === '[') depth++;
    if (c[i] === ']') depth--;
    i++;
  }
  const insertPos = i - 1;
  c = c.substring(0, insertPos) + lessonCode + c.substring(insertPos);
  console.log(`OK ${unitId} +lesson`);
}

